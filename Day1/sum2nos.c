#include <stdio.h>

int main() {

    // Sum of 2 numbers
    
    // Value will 
    int a,b,sum ;
    a = 5 ;
    b = 9 ;
    sum = a+b ;
    printf("Sum is %d",sum) ; // by storing in some variable
    printf("Sum is %d",a+b) ; // by passing an expression

    // value will be given by user
    int n,m,sum ;
    scanf("%d%d",&n,&m) ;
    sum = n+m ;
    printf("Sum is %d",sum) ; // by storing in some variable
    printf("Sum is %d",n+m) ; // by passing an expression



}
